// Exemplo for
public class For {
    public static void main(String[] args) {
         
        for(int carneirinho = 1; carneirinho <= 20; carneirinho++) {
            System.out.println("Carneirinho " + carneirinho);
        }

        System.out.println("Boa noite! depois de contar carneirinhos, durma bem!" );
    }
}
